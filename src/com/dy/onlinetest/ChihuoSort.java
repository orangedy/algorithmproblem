package com.dy.onlinetest;

import java.util.Scanner;



/**
 * @author orangedy
 * @version 2013-4-14 下午10:48:02
 * 求数组的最大有序子序列，不要求连续。
 * 使用动态规划算法
 */
public class ChihuoSort {
	public static void main(String[] args) {
		Scanner cin = new Scanner(System.in);
		int num = cin.nextInt();
		int[] height = new int[num];
		for(int i = 0; i < num; i++){
			height[i] = cin.nextInt();
		}
		
	}
}
